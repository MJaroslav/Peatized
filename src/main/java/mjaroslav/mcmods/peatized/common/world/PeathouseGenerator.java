	public boolean generateChest(World world, Random rand, int x, int y, int z) {
		world.setBlock(x, y, z, Blocks.chest, 0, 3);
		TileEntityChest tileEntity = (TileEntityChest) world.getTileEntity(x, y, z);
		if (tileEntity != null)
			WeightedRandomChestContent.generateChestContents(rand, PeatizedWorldGen.peathouseChestHook.getItems(rand),
					tileEntity, PeatizedWorldGen.peathouseChestHook.getCount(rand));
		return true;
	}

	public boolean generateVillager(World world, Random rand, int x, int y, int z) {
		EntityVillager villager = new EntityVillager(world, PeatizedConfig.villagerId);
		villager.setPositionAndUpdate(x, y, z);
		world.spawnEntityInWorld(villager);
		return true;
	}
